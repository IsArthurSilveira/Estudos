// Sintaxes de saída em JavaScript:

// Exemplo de innerHTML: Insere conteúdo HTML em um elemento específico da página.
document.getElementById("exemploInnerHTML").innerHTML = "Este texto foi inserido usando innerHTML";

// Exemplo de document.write: Escreve diretamente no documento HTML.
document.write("Este texto foi inserido usando document.write");

// Exemplo de window.alert: Exibe uma caixa de alerta com uma mensagem.
window.alert("Este é um alerta usando window.alert");

// Exemplo de console.log: Exibe uma mensagem no console do navegador, útil para depuração.
console.log("Este é um log no console usando console.log");

// Exemplo de console.error: Exibe uma mensagem de erro no console do navegador.
console.error("Este é um erro no console usando console.error");

// Exemplo de console.warn: Exibe uma mensagem de aviso no console do navegador.
console.warn("Este é um aviso no console usando console.warn");

// Exemplo de alert: Exibe uma caixa de alerta com uma mensagem.
alert("Este é um alerta usando alert");

// Exemplo de confirm: Exibe uma caixa de diálogo com uma mensagem e botões 'OK' e 'Cancelar'.
confirm("Este é um diálogo de confirmação usando confirm");

// Exemplo de prompt: Exibe uma caixa de diálogo que solicita ao usuário a entrada de um valor.
prompt("Este é um prompt solicitando entrada do usuário usando prompt");